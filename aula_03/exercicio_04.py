"""Exercício 04.

Média das Notas
Faça um programa que peça quatro notas bimestrais e apresente a média delas.
"""

n1 = int(input("Digite a 1º nota: "))
n2 = int(input("Digite a 2º nota: "))
n3 = int(input("Digite a 3º nota: "))
n4 = int(input("Digite a 4º nota: "))

media = (n1 + n2 + n3 + n4) / 4

print(media)
